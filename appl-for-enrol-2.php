<h1>Application for Enrollment</h1>

<p><strong>IMPORTANT:</strong> The application form is a PDF file, which you must save to your computer's hard disk before filling it out. Once you've filled out the form, print it out as part of your application package. Retain the PDF file, so that you can quickly edit it when you apply again in future years.</p>
<p><a href="./pdf/Mont_xx2014Erollment.pdf" target="_blank">Click here</a> to view and save the Application for Enrollment.</p>

</strong>If you need detailed instructions for saving the form, read the sections below:</strong>

<h2><hr>Applying with Chrome or Firefox</h2>
<strong>NOTE:</strong> The following instructions are for the Windows operating system (Windows 7, specifically), but Macs will have similar steps.
<div>
	<ol>
		<li><a href="./pdf/Mont_xx2014Erollment.pdf" target="_blank">Click here</a> to view the PDF application form a separate browser tab.
		<li>Hover your mouse over the centre-bottom or right-bottom of the screen. A dark grey button-bar will appear.
		<li>As shown in the picture below, click on the disk icon, and save the PDF file to a location of your choice on your computer's hard disk.
		<li>Rename the PDF file using this format: <strong>enrol-&lt;Last&gt;-&lt;First&gt;.pdf</strong>, where <em>Last</em> and <em>First</em> are respectively your child's first and last names. For example, for John Doe's application for enrollment, name your file <em>enrol-Doe-John.pdf</em>.
		<li>Open the PDF form in Adobe Acrobat, fill in the fields, and save it.
		<li>Print out the form and include it with your application package.
	</ol>
