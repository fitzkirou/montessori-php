<p>Elementary students take two 10 week "Canadian Red AquaQuest" sets of lessons 
(12 levels). One session is in the fall; the other is in the spring.</p>

<h2>The Teaching Process</h2>

<p>The lessons are one hour in 
length. The children are divided into multi-level groups and taught accordingly with 
continual evaluation.</p>

<p>Each skill must be seen a minimum of three times by an 
instructor for it to be checked off as completed. It is possible for children to move 
through more than one level per session.</p>


<h2>Evaluation of Students</h2>

<p>At the end of the session the aquatic 
instructors formalize each student's progress in two ways:</p>

<ul>
	<li>by completing a written evaluation</li>
	<li>by distributing badges to those who have completed the necessary requirements</li>
</ul>

<p>Swimming is included in the elementary 
curriculum.</p>

