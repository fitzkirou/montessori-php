<P>In the Montessori classroom, <B><I>sensorial materials</I></B> play
a key role. They are designed to sharpen the senses of the young child.
Further, they enable the child to understand the many impressions received
though the senses.</P>

<H2>Examples of Sensorial Materials</H2>

<P><img src="images/06sensorial1.jpg" width="256" height="341" hspace="15" vspace="15" align="right">Each of the sensorial 
  materials isolates one defining quality, such as:</P>

<UL>
<LI>colour</LI>

<LI>weight</LI>

<LI>shape</LI>

<LI>texture</LI>

<LI>size</LI>

<LI>sound</LI>

<LI>smell.</LI>
</UL>

<P>Sound boxes, for example, are all the same size, shape, colour and texture;
they differ only in the sounds which are made when a child shakes them.
Other sensorial materials include:</P>

<UL>
	<LI>geometric solids</LI>
	<LI>fabrics</LI>
	<LI>square pythagoras</LI>
	<LI>colour tablets</LI>
	<LI>temperature bottles</LI>
	<LI>cylinder blocks.</LI>
</UL>

<H2>Natural Learning</H2>

<P>The Montessori sensorial materials help the child to distinguish, categorize,
and relate new information to what he already knows. The child finds a
sense of order in these materials.</P>

<P>More imporantly, he discovers the joy
of learning that his environment has order. His intellect is trained to
make order out of a multitude of experiences--this is the natural learning
process.</P>

