<h1>Primary Curriculum</h1>

<P>In the Montessori primary curriculum, most materials are self-correcting.
This quality enables children to work on their own--without constant correction
and discouragement from a teacher or other child. The process is self-education,
building self-confidence through competence. The Montessori <A NAME="menulist"></A>approach,
therefore, forms a solid foundation for the child's education and life.</P>

<P>The primary curriculum at Guelph Montessori includes concrete materials, like 
  the Large Bead Frame being used in this photo. By working--primarily on their 
  own--with these materials, children can more easily grasp absract ideas.</P>

