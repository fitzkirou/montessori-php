<h2>Studying the Earth</h2>

<p>The children's study of geography will move from the universe to the earth.  A 
variety of concepts are explored through experimentation and demonstration. The 
topics covered are as follows:</p>

<ul>
	<li><b>The Movement of the Earth and its Consequences</b> 	(day/night, time differences, seasons.)
	<li><b>The Lithosphere</b> (history of the earth rocks and minerals, composition of the earth's crust, plate tectonics, mountain building)
	<li><b>The Atmosphere</b> (characteristics, layers, importance as a protector and insulator, wind, high and low pressure zones)
	<li><b>The Hydrosphere</b>  (cloud formation, cloud types, the water cycle, types of rain, water currents, erosion, rivers, glaciers.)
	<li><b>The Biosphere</b>  (vegetation, animals and people in different regions.)
	<li><b>Human Geography</b>  (interdependencies, producers, consumers, trade, government and common services, import, export.)
</ul>

