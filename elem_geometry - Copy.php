<p>The elementary child has already been provided with a rich sensorial experience of geometry through his or her work with materials in the primary classroom.  The work is expanded upon in the elementary program.  The child learns the proper names of geometric solids and figures, and explores the triangle as the "constructor" or building block for all geometric figures.</p>

<h2>Exploring Geometric Concepts</h2>

<p>Once the child has acquired knowledge of geometric vocabulary, he or she will 
explore geometric concepts such as:</p>

<ul>
	<li>the point</li>
	<li>lines</li>
	<li>angles</li>
	<li>triangles</li>
	<li>quadrilaterals</li>
	<li>polygons</li>
	<li>circles</li>
</ul>

<h2>Relating Concepts, Working with Materials</h2>

<p>The child learns how each concept relates to 
each other by working with materials that allow him or her to build and experiment 
with each concept.  Each topic is studied in great detail.</p>

<p>The Montessori student will also explore concepts such as congruency, similarity and equivalence, transitivity, area, volume, Pythagorean theorem and Euclid's theorem.</p>

