<p>The study of biology begins by exposing the child to a variety of plants and 
animals from each of the phyla and classes. </p>

<h2>Games, Research, and Detailed Study</h2>

<p>The child is presented with a series of 
games and research questions that allow him or her to gain knowledge about 
various organisms. Knowledge of each type of animal and plant is expanded through detailed study.
</p>

<p>In these detailed studies, each type of plant and animal is analyzed with 
respect to:</p>

<ul>
	<li>characteristics</li>
	<li>parts</li>
	<li>function</li>
</ul>

<h2>Taxonomical Classification System</h2>

