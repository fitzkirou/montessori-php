<h1>Montessori Method</h1>

<P>Maria Montessori was a medical doctor, anthropologist, and psychologist.
She became interested in the development and psychology of the young child.</P>

<H2>Allowing Individual Growth</H2>

<P><img src="images/06method-general.jpg" width="341" height="256" hspace="15" vspace="20" align="right">The 
  teaching method she evolved stresses the child's individual growth of:</P>

<UL>
<LI>physical abilities</LI>

<LI>intellectual abilities</LI>

<LI>psychological abilities.</LI>
</UL>

<P>The Montessori method allows the child to progress at his own speed
and by his own motivation. This progress occurs within a carefully prepared
environment that offers:</P>

<UL>
<LI>beauty</LI>

<LI>order</LI>

<LI>concrete reality.</LI>
</UL>

<H2>Developing the Child's Senses</H2>

<P>Dr. Montessori observed that children go through various sensitive stages, 
  in which they are very receptive to learning specific skills. She stressed the 
  importance of developing the senses between the ages of two and six--when the 
  child naturally wishes to use and perfect his or her senses. Thus, the materials 
  used by the children are designed for them to learn by:</P>

<UL>
<LI>seeing</LI>

<LI>touching</LI>

<LI>hearing</LI>

<LI>feeling</LI>

<LI>moving.</LI>
</UL>

<P>The Montessori <B><I>prepared environment</I></B> allows children to
meet their needs through individual, spontaneous activities. The child's
sensitivities guide his choice. To build concentration and self-discipline
as they learn skills, the children work with scientifically selected materials.
Their education is an active--rather than passive--process. The child educates
himself.</P>

<P>For most of the school day, class time is an individual or small group
format. During part of each day, however, the entire class gets together
for group activites:</P>

<UL>
<LI>singing</LI>

<LI>story telling</LI>

<LI>movement activities.</LI>
</UL>

<H2>Natural Tools for Learning</H2>

<P>The goal of the Montessori method is not to fill the child with facts
from some pre-selected course of study--but rather to cultivate the child's
own natural desire to learn. Dr. Montessori saw that young children have
three <I><B>natural tools for learning</B>:</I></P>

<UL>
<LI>the senses</LI>

<LI>the hands</LI>

<LI>the intellect.</LI>
</UL>

<P>The Montessori method guides the child to perfect these learning tools
and to grow towards self-discipline and independence. With this guidance,
the child will develop a sturdiness of character and love for learning--a
love that will last him all his life.</P>

