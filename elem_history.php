<p>The History curriculum begins by allowing the child to experience and learn about 
the concepts of time in order to prepare the child for the study of history.</p>

<h2>Concepts of Time and Culture</h2>

<p>The 
passage of time is conveyed to the early elementary student through a series of 
activities.  Some of these activities include:</p>

<ul>
	<li>class diaries</li>
	<li>study of days and months</li>
	<li>introduction to the clock</li>
	<li>history of the child</li>
	<li>exploration of the fundamental tenses (past, present and future)</li>
</ul>

<p>The foundation of the history work begins with a study of the needs and 
tendencies of humans throughout time.  The student explores how humans share 
common needs, and studies human adaptation in different times and cultures.</p>

<h2>Natural and Human History</h2>

<p>Natural History is the next area of study.  The child discovers how much time passed before human history begins, and how human history accounts for a very small portion of time.</p>

<p>The study of human history begins by discussing the evolutionary theory.  The child learns about human evolution from Australopithecine to Homo sapiens.  The child explores artefacts to discover how early humans might have lived and how time periods acquired their name.</p>

<p>The child continues a study of human history by looking at ways that humans combined and grouped together to ensure survival and continuation.   This includes study of ancient and modern civilizations.</p>

