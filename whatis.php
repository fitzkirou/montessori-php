<h1>Montessori Basics</h1>

<p>At the dawn of the twentieth century, Dr. Maria Montessori began her
revolutionary work with impoverished children in Rome, Italy. As a result of 
Dr. Montessori's keen observation of the children as well as her background
in medicine and psychology, she quickly recognized that the key formative
years are those between birth and the age of six. </p>

<h2>The Absorbent Mind</h2>

<p>The child before six has a great capacity for mental absorption--what
Dr. Montessori called the <i>absorbent mind</i>. The young child's
absorbent mind will never repeat its extraordinary ability to:</p>

<ul>
<li>absorb language and movement</li>

<li>internalize order</li>

<li>learn through sensorial exploration</li>
</ul>

<p>Never will these sensitivities be more alive than in the preschool years,
and never again will learning be so easy--or so much fun.</p>

<h2>What Dr. Montessori Accomplished</h2>

<p>Under
Maria Montessori's guidance, shy and neglected children soon began reading,
writing, and doing arithmetic.</p>

<p>As well, these children were considerably
more:</p>

<ul>
<li>alert</li>

<li>out-going</li>

<li>self-confident</li>

<li>self-disciplined</li>

<li>sociable</li>

<li>respectful of each other</li>

<li>respectful of their environment.</li>
</ul>

<p>When these accomplishments became known in Euope, Dr. Montessori captured
that continent's attention. Today, <i>Montessori</i> schools flourish
around the world. In the Netherlands, for example, it is a complete alternative
to the public system--from preschool through to College.</p>

<p>As educational experts constantly remind us, the early childhood years
have the greatest influence on a person's development. Perhaps this is
why <i>Montessori</i> has become so enormously popular with conscientious
parents--people who want their children not simply to be educated but to
have a firm foundation to life.</p>


