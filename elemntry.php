<H2>Meeting the Needs

<BR>of 6- to 12-year-olds</H2>



<P>Specific characteristics developed to meet the developmental

needs of the 6- to 12-year-old include:</P>



<UL>

<LI>three-year cycles with the same teacher (ages six to nine and nine

to 12), which allows the teacher intimate knowledge of each child</LI>



<LI>group lessons and group work which facilitate broad social development

and cooperative learning</LI>



<LI>use of impressionistic charts as a teaching aid, as children's imaginations

develop</LI>



<LI>an interrelated curriculum</LI>



<LI>a &quot;going out&quot; program which increases student awareness of

their community</LI>



<LI>a continued progression of beautiful Montessori materials, guiding

the child from concrete learning to increasing abstraction</LI>



<LI>shared class meetings to develop personal responsibility</LI>

</UL>



<H2>Developing Concentration</H2>



<P>The children have free play or recess for an hour after lunch each day.

There is no recess during the morning.</P>



<P>This system allows three hours of uninterrupted 

work time which helps to develop concentration, as the children are able

to stay at their chosen work activities for extended periods of time.</P>



<H2>A Warm Classroom Environment</H2>



<P>The classroom offers a warm environment for children to learn and develop.

They are respected as active participating members and are encouraged to

share in the responsibilities of its care.</P>



<P>Class meetings allow expression

of thoughts, ideas, feelings, and discussions involving current events. 

Montessori graduates feel they can make a difference in the world.</P>

